#SLICING

#positive indexing and slicing
a="prince"
print(a[3:6])
print(a[1:6:2])
print(a[:6:1])

#negative indexing and slicing
print(a[-6:-1:1])
print(a[-1:-7:-1])
print(a[-1:-7:-2])

#postive and negative indexing
print(a[1:-1])

b=("0123456789")
#print odd number
print(b[2:10:2])
print(b[-2:-9:-2])
